import kotlin.reflect.KClass

inline fun <reified T : Any> deepNesting(n: Int) = matt.deepNesting(n, T::class)

fun <T : Any> deepNesting(
    n: Int,
    tClass: KClass<T>
): String {
    return if (n == 0) {
        ""
    } else {
        deepNesting<T>(n - 1, tClass) + tClass.simpleName
    }
}

